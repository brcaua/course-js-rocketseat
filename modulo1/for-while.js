// Using "For"
// When to use: When it is possible to measure the size of the interval
for (var num = 0; num <= 100; num++) {
  console.log(num);
}

// Using "While"
// When to use: When it is impossible to measure the size of the interval
var a = 435435346564564;
while (a > 50) {
  console.log(a);
  a /= 5;
}
